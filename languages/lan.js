const lan = prompt('Какой язык использовать?')

switch (lan) {
    case 'RU':
       console.log('Добрый день!');
    break;
    case 'EN':
       console.log('Good afternoon!');
    break;
    case 'DE':
       console.log('Guten Tag!');
    break;
    case 'FR':
       console.log('Bonjour!');
    break;
}